def Hanoi(a,b,c,n):
    """汉诺塔问题，递归求a盘中n个珠宝移到b盘的次数"""
    # 把a当作输出盘，b作为目标盘，c是辅助盘（中介盘）
    if n == 1:
        print(a+"->"+b) # 如果是只有一个,那么输出盘到目标盘
    else:
        Hanoi(a,c,b,n-1) # b当作辅助盘，将上面n-1个移动到c
        print(a+"->"+b) # 然后现在a中只有第n个，输出盘到辅助盘
        Hanoi(c,b,a,n-1) # 前n-1个都在c中，所以输出盘变成c，将它们移动到b


Hanoi("a","b","c",3)

